    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            Genre genre = new Genre(
                    tfName.getText().trim(),
                    ((Movie)cbMovies.getSelectedItem()).getId()
            );
            repository.createGenre(genre);
            genresTableModel.setGenres(repository.selectGenresFromMovie((Movie)cbMovies.getSelectedItem()));

            clearForm();
        } catch (Exception ex) {
            Logger.getLogger(PanelMenu.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to create article!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (selectedGenre == null) {
            MessageUtils.showInformationMessage("Wrong operation", "Please choose genre to update");
            return;
        }
        try {
            Movie selectedMovie = (Movie)cbMovies.getSelectedItem();
            selectedGenre.setName(tfName.getText().trim());
            selectedGenre.setMovieID(selectedMovie.getId());

            repository.updateGenre(selectedGenre.getIDGenre(), selectedGenre);
            genresTableModel.setGenres(repository.selectGenresFromMovie(selectedMovie));

            clearForm();
        } catch (Exception ex) {
            Logger.getLogger(EditMoviesPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to update genre!");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedGenre == null) {
            MessageUtils.showInformationMessage("Wrong operation", "Please choose genre to delete");
            return;
        }
        if (MessageUtils.showConfirmDialog(
                "Delete genre",
                "Do you really want to delete genre?") == JOptionPane.YES_OPTION) {
            try {
                repository.deleteGenre(selectedGenre.getIDGenre());
                genresTableModel.setGenres(repository.selectGenresFromMovie((Movie)cbMovies.getSelectedItem()));

                clearForm();
            } catch (Exception ex) {
                Logger.getLogger(EditMoviesPanel.class.getName()).log(Level.SEVERE, null, ex);
                MessageUtils.showErrorMessage("Error", "Unable to delete genre!");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

