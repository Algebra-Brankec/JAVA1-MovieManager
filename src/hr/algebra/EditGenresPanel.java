    public void showArticle() {
        clearForm();
        int selectedRow = tbGenres.getSelectedRow();
        int rowIndex = tbGenres.convertRowIndexToModel(selectedRow);
        int selectedGenreId = (int) genresTableModel.getValueAt(rowIndex, 0);

        try {
            Optional<Genre> optArticle = repository.selectGenre(selectedGenreId);
            if (optArticle.isPresent()) {
                selectedGenre = optArticle.get();
                fillForm(selectedGenre);
            }
        } catch (Exception ex) {
            Logger.getLogger(PanelMenu.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to show movie!");
        }
    }
    
    private void loadTable() {
        tbGenres.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tbGenres.setAutoCreateRowSorter(true);
        tbGenres.setRowHeight(25);
        
        try {
            Movie selectedGenre = (Movie)cbMovies.getSelectedItem();
            genresTableModel = new GenreTableModel(repository.selectGenresFromMovie(selectedGenre));
        } catch(Exception e) {
            genresTableModel = new GenreTableModel(new ArrayList<>());
        }
        
        tbGenres.setModel(genresTableModel);
    }
    
    private void init() {
        try {
            initValidation();
            initRepository();
            initComboBox();
        } catch (Exception ex) {
            Logger.getLogger(EditMoviesPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Unrecoverable error", "Cannot initiate the form");
            System.exit(1);
        }
    }
    
    private void initValidation() {
        validationComboBox = Arrays.asList(cbMovies);
        validationFields = Arrays.asList(tfName);
    }
    
    private void clearForm() {
        validationFields.forEach(e -> e.setText(""));
        selectedGenre = null;
    }
    
    private void initRepository() throws Exception {
        repository = RepositoryFactory.getRepository();
    }
    
    private void initComboBox() throws Exception {
        List<Movie> movies = repository.selectMovies();
        
        MutableComboBoxModel<Movie> model = new DefaultComboBoxModel<>();
        for(Movie movie : movies)
        {
            model.addElement(movie); // ERROR
        }
        
        cbMovies.setModel(model);
        
        cbMovies.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent ie) {
                loadTable();
            }
        });
    }
    
    private void fillForm(Genre genre) {
        //cbMovies.set("test");
        tfName.setText(genre.getName());
    }

